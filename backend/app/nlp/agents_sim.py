class AURAPDF(FPDF if FPDF else object):
    def header(self):
        if not FPDF:
            return
        # Header banner styling
        self.set_fill_color(16, 24, 48)  # Deep slate/navy
        self.rect(0, 0, 210, 30, 'F')
        
        self.set_y(10)
        self.set_font("helvetica", "B", 14)
        self.set_text_color(255, 255, 255)
        self.cell(0, 10, "AURA AI AUTONOMOUS AGENT ENGINE", align="C")
        self.ln(15)

    def footer(self):
        if not FPDF:
            return
        self.set_y(-15)
        self.set_font("helvetica", "I", 8)
        self.set_text_color(128, 128, 128)
        self.cell(0, 10, f"Generated automatically by AURA AI. Page {self.page_no()}/{{nb}}", align="C")

def resolve_workspace_path(path: str) -> str:
    if not path:
        return path
    if path.startswith("/workspace/"):
        local_dir = os.path.join(os.getcwd(), "workspace")
        os.makedirs(local_dir, exist_ok=True)
        return os.path.join(local_dir, path.replace("/workspace/", ""))
    return path

